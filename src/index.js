const MORSE_TABLE = {
  ".-": "a",
  "-...": "b",
  "-.-.": "c",
  "-..": "d",
  ".": "e",
  "..-.": "f",
  "--.": "g",
  "....": "h",
  "..": "i",
  ".---": "j",
  "-.-": "k",
  ".-..": "l",
  "--": "m",
  "-.": "n",
  "---": "o",
  ".--.": "p",
  "--.-": "q",
  ".-.": "r",
  "...": "s",
  "-": "t",
  "..-": "u",
  "...-": "v",
  ".--": "w",
  "-..-": "x",
  "-.--": "y",
  "--..": "z",
  ".----": "1",
  "..---": "2",
  "...--": "3",
  "....-": "4",
  ".....": "5",
  "-....": "6",
  "--...": "7",
  "---..": "8",
  "----.": "9",
  "-----": "0",
};

function decode(expr) {
  let letters = [];
  for (let i = 0; i < expr.length; i += 10) {
    const code = expr
      .slice(i, i + 10)
      .replaceAll("10", ".")
      .replaceAll("11", "-")
      .replaceAll("0", "")
      .replaceAll("*", "");
    letters.push(code ? MORSE_TABLE[code] : " ");
  }
  return letters.join("");
}

module.exports = {
  decode,
};
